#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_dec_to_hex(char c)
{
	char	*base;

	base = "0123456789abcdef";
	ft_putchar(base[c / 16]);
	ft_putchar(base[c % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	int	index;

	index = 0;
	while (str[index] != '\0')
	{
		if (str[index] > 126 || str[index] < 32)
		{
			ft_putchar('\\');
			ft_dec_to_hex(str[index]);
		}
		else
			ft_putchar(str[index]);
		index++;
	}
}
